package com.tnsif.day3;

public class Loops {

	public static void main(String[] args) {

		//-----------  for loop  ---------
		
		for(int i = 1; i <= 10; i++)           // for loop syntax : (initialization, condition, increment/decrement)
		{
			System.out.println(i);             // "println" -- prints the statement and goes to the next line
		}
		
		
		//------------------------------------------------------------------------------------------------------------------------
		
		for(int i = 1; i <= 10; i++)
		{
			System.out.print(i);     			// to print the elements side by side we just use "print" statement
		}
	}

}



