package com.tnsif.day3;

public class Do_While_Loop {

	public static void main(String[] args) {
		// ---------------------  Do-While  ----------------------------//
		//In a do-while loop:

			//1 . The do block executes first
			//2 . Then the condition is checked
		
		int j = 1;
		do {
			System.out.println(j);
			j++;
		}while(j<=10);             // semicolon should be kept after while condition( in do-while loop )
	}

}
