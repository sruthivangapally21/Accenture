public class While_Loop {

	public static void main(String[] args) {
		
		// -------------------------------  while loop   --------------------------------
		
		int num = 20;
		int n= 1;
		 while(n <= num)                    // while syntax : while (condition){-- your code--}
		 {
			 //System.out.println(n);
			 System.out.print(n+",");
			 n++;
		 }
		 
		 System.out.println("");
		 System.out.println("Hey i am while loop");
			
		
	}

}
